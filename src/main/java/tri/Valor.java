package tri;

public class Valor {

	private int maior;
	private int index;

	public Valor(int maior, int index) {
		super();
		this.maior = maior;
		this.index = index;
	}

	public int getMaior() {
		return maior;
	}

	public void setMaior(int maior) {
		this.maior = maior;
	}

	public int getIndex() {
		return index;
	}

	public void setIndex(int index) {
		this.index = index;
	}

}
